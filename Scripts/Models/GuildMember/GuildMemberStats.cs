public class GuildMemberStats
{
    protected double strength;
    public double Strength
    {
        get => strength;
        set => strength = value;
    }

    protected double agility;
    public double Agility
    {
        get => agility;
        set => agility = value;
    }

    protected double health;
    public double Health
    {
        get => health;
        set => health = value;
    }

    protected double inteligence;
    public double Inteligence
    {
        get => inteligence;
        set => inteligence = value;
    }
}